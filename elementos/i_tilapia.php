            <h1> <b>Tilapia</b></h1><br><br>
            <h4> <b>La tilapia roja o mojarra roja (Orechromis sp.) es un híbrido resultante del cruce de varias especies del género Oreochromis originarias de África e Israel, con características especiales que lo ponen en ventaja frente a otras especies como: ganancia en peso de 600 gramos/año, rendimientos superiores a 600 toneladas/hectárea/año en sistemas de producción intensivos desarrollados en jaula o jaulones y a la gran facilidad para filetear (Incoder, 2014). En el cultivo de tilapia roja, se emplean fundamentalmente dos sistemas de producción; el sistema de estanques en tierra y el sistema de jaulas o jaulones flotantes en cuerpos de agua (embalses de hidroeléctricas o reservorios de agua para distritos de riego). En el desarrollo del presente artículo se adelantará la descripción de los requerimientos básicos para el cultivo de la tilapia roja en estanques, alevinos, infraestructura productiva, proceso productivo, enfermedades y plagas.</b></h4><br>

			<h4><b>Requerimientos básicos para el cultivo de la tilapia roja en estanques <br><br>


			<center><img src="../imagenes/tilapia.jpg"></center>


				<ul>

					<li>Es importante tener en cuenta algunos aspectos básicos, que orientan la adecuada ubicación de los estanques en donde se adelanta satisfactoriamente el cultivo de la tilapia, estos son: a) disponibilidad y calidad del agua, b) tipo de suelo, c) pendiente y d) accesibilidad. </li><br>
					<li> Ua. Respecto a la disponibilidad de agua, en cuanto a la cantidad y calidad, esta debe ser tomada de quebradas, ríos, embalses, lagunas o ciénagas a una distancia razonable de 0 a 500 metros y no mayor a 2 kilómetros de los estanques (MADR-AUNAP-FAO, 2013), con una captación mínima de 1 litro por segundo a un máximo de 60 litros por segundo de caudal. En cuanto a la calidad, el agua a utilizar no debe contener: residuos de insumos agropecuarios como insecticidas, fungicidas o herbicidas, residuos industriales, mineros o domésticos como detergentes derivados del petróleo. De igual forma, la temperatura óptima de 22 a 26°C, oxígeno disuelto mínimo de 4 ppm1 , acidez de 5,0 a 9,0 de pH con un valor ideal de 7,5 (Guevara, 2009). La turbidez del agua es un parámetro que permite establecer su calidad, dado que a mayor turbidez aumenta la temperatura, se reduce la concentración de oxígeno, aumentan las enfermedades y se pone en riesgo la vida de los peces. Esta es adecuada cuando al introducir en el estanque un disco Secchi2 o la mano, a una profundidad de 30 a 45 centímetros, el disco o los dedos de la mano se observan con facilidad; la turbidez del agua aumenta cuando se tienen densidades de siembra muy altas, excesos en la alimentación y en la aplicación de fertilizantes. En este sentido, se programan monitoreos, realizando muestreos diarios en la mañana y tarde para la medición del oxígeno disuelto, el pH o acidez, temperatura y turbidez del agua a la entrada, en el estanque y a la salida, por lo que se debe contar con un kit portátil de laboratorio (ICA, 2007). </li><br>

					<li> b. El suelo sobre el cual se construye el estanque debe contener mínimo 30% de arcillas o estar en un rango óptimo del 35 a 50%; con esta cantidad de arcilla en el suelo se evita la pérdida de agua por filtraciones (MADR-AUNAP-FAO, 2013). Es muy importante conocer los antecedentes de uso del suelo y realizar análisis químicos con el fin de descartar la presencia de metales pesados y plaguicidas que puedan contaminar, afectar la salud de los peces y amenazar la inocuidad del producto. Así mismo, para la ubicación del sitio se debe tener en cuenta el Plan de Ordenamiento Territorial (EOT, PBOT o POT) del municipio (ICA, 2007). </li><br>

					<li>c. La pendiente del terreno más indicada para la ubicación de los estanques está en el rango de 0 a 7%, siendo moderadamente aptas las pendientes de hasta el 25%. La pendiente es un factor que determina la facilidad y el costo de construcción de los estanques; a mayor pendiente, mayor dificultad y mayor costo. </li><br>

					<li> d. La granja piscícola debe contar con acceso por vía terrestre, servicio de energía eléctrica, cercanía a mercados locales y regionales para la comercialización de la producción y compra de insumos, y a puertos o aeropuertos si la producción está orientada a la exportación. e. Por otra parte, se deben respetar las áreas naturales protegidas, evitar zonas inundables durante gran parte del año y áreas urbanas, en donde no se podrá adelantar la piscicultura o el cultivo de peces (MADRAUNAP-FAO, 2013).</li><br>

					

