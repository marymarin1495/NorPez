				<li><h3><b>Alevinos <b></h3><li>

						
						<li> La producción de alevinos o semilla se adelanta bien sea por reproducción en cautiverio o por incubación, siendo más seguro para la sobrevivencia este último, el cual consiste en obtener los huevos fertilizados de la boca de las hembras y luego llevarlos a incubadoras con condiciones controladas y circulación permanente de agua, en donde se favorece el desarrollo de las ovas3 a larvas (MADR - CCI, 2012A), que luego son sometidas a un procedimiento de diferenciación de sexo o de reversión sexual, mediante el suministro de hormonas en el alimento durante un tiempo de 30 días, en donde se espera un 95% de alevinos machos con un peso de 1 gramo. Generalmente los piscicultores grandes adelantan la producción de alevinos en sus propias granjas, mientras que los medianos y pequeños productores los obtienen de empresas especializadas en la producción de alevinos o a través de intermediarios (MADR-Observatorio Colombiano de Ciencia y Tecnología-OCyT, 2008). </li><br>

						<li>
							Para la compra e ingreso de alevinos a la finca, se debe tener en cuenta que estén libres de patógenos y enfermedades, para lo que se debe contar con la guía de movilización expedida por el ICA. Por otra parte, deben mostrar un desarrollo con condiciones físicas aceptables de acuerdo con la edad y dejarlos en observación en un sitio aislado durante un tiempo para evitar el ingreso de enfermedades y plagas; de esto depende la obtención de una buena cosecha (ICA, 2007).</li><br>

